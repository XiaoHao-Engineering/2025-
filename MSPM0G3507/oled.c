/* 6x8 ASCII字库声明（外部定义）*/
extern const uint8_t OLED_F6x8[][6];

/**
  * @brief  向OLED发送一个字节命令
  * @param  cmd : 命令字节
  */
static void OLED_WriteCmd(uint8_t cmd)
{
    uint8_t data[2];
    data[0] = 0x00;  /* 控制字节：命令模式 */
    data[1] = cmd;
    DL_I2C_fillControllerTXFIFO(OLED_I2C_INST, data, 2);
    while (DL_I2C_isControllerBusy(OLED_I2C_INST));
}

/**
  * @brief  向OLED发送一个字节数据
  * @param  dat : 数据字节
  */
static void OLED_WriteData(uint8_t dat)
{
    uint8_t data[2];
    data[0] = 0x40;  /* 控制字节：数据模式 */
    data[1] = dat;
    DL_I2C_fillControllerTXFIFO(OLED_I2C_INST, data, 2);
    while (DL_I2C_isControllerBusy(OLED_I2C_INST));
}

/**
  * @brief  初始化OLED
  */
void OLED_Init(void)
{
    /* 延时等待OLED上电稳定 */
    uint32_t delay = 100000;
    while (delay--);

    /* SSD1306初始化序列 */
    OLED_WriteCmd(0xAE); /* 关闭显示 */
    OLED_WriteCmd(0x20); /* 设置内存寻址模式 */
    OLED_WriteCmd(0x00); /* 水平寻址模式 */
    OLED_WriteCmd(0xB0); /* 设置页起始地址 */
    OLED_WriteCmd(0xC8); /* COM扫描方向：从COM[N-1]到COM0 */
    OLED_WriteCmd(0x00); /* 设置低列地址 */
    OLED_WriteCmd(0x10); /* 设置高列地址 */
    OLED_WriteCmd(0x40); /* 设置显示起始行 */
    OLED_WriteCmd(0x81); /* 设置对比度 */
    OLED_WriteCmd(0xFF); /* 对比度最大值 */
    OLED_WriteCmd(0xA1); /* 段重映射：列127->SEG0 */
    OLED_WriteCmd(0xA6); /* 正常显示（非反相）*/
    OLED_WriteCmd(0xA8); /* 设置多路复用比 */
    OLED_WriteCmd(0x3F); /* 1/64 duty */
    OLED_WriteCmd(0xA4); /* 全局显示：跟随RAM内容 */
    OLED_WriteCmd(0xD3); /* 设置显示偏移 */
    OLED_WriteCmd(0x00); /* 无偏移 */
    OLED_WriteCmd(0xD5); /* 设置时钟分频/振荡频率 */
    OLED_WriteCmd(0xF0); /* 分频因子=0xF */
    OLED_WriteCmd(0xD9); /* 设置预充电周期 */
    OLED_WriteCmd(0x22); /* [3:0]=2 DCLK, [7:4]=2 DCLK */
    OLED_WriteCmd(0xDA); /* 设置COM引脚硬件配置 */
    OLED_WriteCmd(0x12); /* 顺序COM配置 */
    OLED_WriteCmd(0xDB); /* 设置VCOMH反压 */
    OLED_WriteCmd(0x20); /* 0.77xVCC */
    OLED_WriteCmd(0x8D); /* 启用电荷泵 */
    OLED_WriteCmd(0x14);
    OLED_WriteCmd(0xAF); /* 开启显示 */

    OLED_Clear();
    OLED_ShowString(0, 0, "System Ready");
}

/**
  * @brief  清屏
  */
void OLED_Clear(void)
{
    uint8_t i, j;
    for (i = 0; i < 8; i++)    /* 8页 */
    {
        OLED_WriteCmd(0xB0 + i); /* 设置页地址 */
        OLED_WriteCmd(0x00);     /* 低列地址 */
        OLED_WriteCmd(0x10);     /* 高列地址 */
        for (j = 0; j < 128; j++)
        {
            OLED_WriteData(0x00);
        }
    }
}

/**
  * @brief  在指定位置显示字符
  * @param  x    : 列坐标 (0~127)
  * @param  y    : 行坐标 (0~7)
  * @param  ch   : 要显示的字符
  */
static void OLED_ShowChar(uint8_t x, uint8_t y, char ch)
{
    uint8_t c = ch - 32;  /* 字库从空格开始 */
    uint8_t i;

    if (x > OLED_WIDTH - 6 || y > 7) return;
    if (c > 94) return;

    OLED_WriteCmd(0xB0 + y);
    OLED_WriteCmd(((x & 0xF0) >> 4) | 0x10);
    OLED_WriteCmd(x & 0x0F);

    for (i = 0; i < 6; i++)
    {
        OLED_WriteData(OLED_F6x8[c][i]);
    }
}

/**
  * @brief  显示字符串
  */
void OLED_ShowString(uint8_t x, uint8_t y, const char *str)
{
    while (*str)
    {
        OLED_ShowChar(x, y, *str);
        x += 6;
        if (x > 122) { x = 0; y++; }
        str++;
    }
}

/**
  * @brief  显示数字
  */
void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len)
{
    uint8_t i;
    uint8_t buf[10];
    uint8_t cnt = 0;

    do
    {
        buf[cnt++] = num % 10;
        num /= 10;
    } while (num > 0);

    /* 补前导零 */
    while (cnt < len)
    {
        buf[cnt++] = 0;
    }

    for (i = 0; i < cnt; i++)
    {
        OLED_ShowChar(x + (cnt - 1 - i) * 6, y, '0' + buf[i]);
    }
}

/**
  * @brief  显示圈数信息
  */
void OLED_ShowLapInfo(uint8_t target, uint8_t current, const char *state)
{
    OLED_ShowString(0, 0, "Target Lap:");
    OLED_ShowNum(78, 0, target, 1);
    OLED_ShowString(84, 0, "/5");

    OLED_ShowString(0, 2, "Current Lap:");
    OLED_ShowNum(84, 2, current, 1);

    OLED_ShowString(0, 4, "State:");
    OLED_ShowString(42, 4, state);
}

